


numero1 = float(input("Primer numero: "))
numero2 = float(input("Segundo numero: "))
numero3 = float(input("Tercer numero: "))
numero4 = float(input("Cuarto numero: "))

print("Son iguales ?: ", numero1, "==", numero4, ":", numero1 == numero4)
print("Son diferentes ?:", numero2, "!=", numero3, ":", numero2 != numero3)
print("Es mayor que ?:", numero3, ">", numero2, ":", numero3 > numero2)
print("Es menor que ?:", numero4, "<", numero1, ":", numero4 < numero1)
print("Es mayor o igual ?", numero1, ">=", numero3, ":", numero1 >= numero3)
print("Es menor o igual ?", numero2, "<=", numero4, ":", numero2 <= numero4)






